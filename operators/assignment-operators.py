# Assignment operator and bitwise operators:

number = 5

number += 3

# number now equals 8

number -= 2

# number now equals 6

number *= 3

# number now equals 18

number /= 2

# number now equals 9

###

newNumber = 10

newNumber %= 2

# newNumber now equals 0

###

thirdNumber = 4

thirdNumber **= 5

# thirdNumber now equals 1024
